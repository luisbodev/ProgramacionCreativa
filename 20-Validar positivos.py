num1 = int(input("Escriba un número: "))
num2 = int(input("Escriba otro número: "))

if (num1 >= 0 and num2 >= 0) :
    print("Los números son positivos")
else :
    print("Los números no son positivos")
    
    
if (num1%2==0 and num2%2==0) :
    print("Los números son pares")
else :
    print("Los números no son pares")