# Hacer un programa que pida dos números y escriba si uno es mayor o igual°
# que otro.
# Las dos frases que puede imprimir son:
# • "El primer número es mayor o igual que el segundo"
# • "El primer número es menor que el segundo".

num1 = int(input("Ingrese un número: "))
num2 = int(input("Ingrese otro número: "))

if num1 >= num2 :
    print("El primer número es mayor o igual que el segundo")
else:
    print("El primer número es menor que el segundo")
